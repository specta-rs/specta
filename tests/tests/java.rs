#![allow(deprecated, non_camel_case_types)]

use std::{borrow::Cow, path::Path, process::Command};

use serde::{Deserialize, Serialize};
use specta::{Format, Type, Types};
use specta_java::{Java, Layout, OptionalStyle};
use tempfile::TempDir;

struct IdentityFormat;

impl Format for IdentityFormat {
    fn map_types(&'_ self, types: &Types) -> Result<Cow<'_, Types>, specta::FormatError> {
        Ok(Cow::Owned(types.clone()))
    }

    fn map_type(
        &'_ self,
        _: &Types,
        datatype: &specta::datatype::DataType,
    ) -> Result<Cow<'_, specta::datatype::DataType>, specta::FormatError> {
        Ok(Cow::Owned(datatype.clone()))
    }
}

/// A documented generic response.
#[derive(Type, Serialize, Deserialize)]
#[specta(collect = false)]
struct Response<T> {
    /// Returned payload.
    value: T,
    optional_count: Option<u32>,
    labels: Vec<String>,
}

#[derive(Type, Serialize, Deserialize)]
#[specta(collect = false)]
#[serde(rename_all = "snake_case")]
enum Status {
    Ready,
    PendingReview,
}

#[derive(Type, Serialize, Deserialize)]
#[specta(collect = false)]
#[serde(tag = "kind", content = "payload")]
enum Event<T> {
    Started,
    Progress(u8),
    Finished { result: T },
}

#[derive(Type, Serialize, Deserialize)]
#[specta(collect = false)]
struct Model {
    response: Response<String>,
    status: Status,
    event: Event<String>,
    lookup: std::collections::HashMap<String, i64>,
    tuple: (String, bool),
}

fn types() -> Types {
    Types::default()
        .register::<Response<String>>()
        .register::<Status>()
        .register::<Event<String>>()
        .register::<Model>()
}

fn temp_dir() -> TempDir {
    let root = Path::new(env!("CARGO_MANIFEST_DIR")).join(".temp");
    std::fs::create_dir_all(&root).unwrap();
    TempDir::new_in(root).unwrap()
}

fn compile_java(root: &Path, files: &[&Path]) {
    let output = Command::new("javac")
        .arg("--release")
        .arg("17")
        .arg("-d")
        .arg(root.join("classes"))
        .args(files)
        .output()
        .expect("javac must be installed to validate generated Java");
    assert!(
        output.status.success(),
        "generated Java did not compile:\n{}\n{}\n{}",
        String::from_utf8_lossy(&output.stdout),
        String::from_utf8_lossy(&output.stderr),
        files
            .iter()
            .map(|path| std::fs::read_to_string(path).unwrap())
            .collect::<Vec<_>>()
            .join("\n"),
    );
}

