#[test]
fn flat_file_compiles_and_supports_configuration() {
    let temp = temp_dir();
    let path = temp.path().join("ApiTypes.java");
    Java::default()
        .package("dev.specta.generated")
        .class_name("ApiTypes")
        .header("// custom header")
        .with_raw("public static final String VERSION = \"1\";")
        .export_to(&path, &types(), specta_serde::Format)
        .unwrap();

    let source = std::fs::read_to_string(&path).unwrap();
    assert!(source.starts_with(
        "// custom header\n// @generated by specta-java (package: dev.specta.generated)\n\npackage"
    ));
    assert!(source.contains("public final class ApiTypes"));
    assert!(source.contains("VERSION"));
    compile_java(temp.path(), &[&path]);
}

#[test]
fn files_layout_writes_public_types_that_compile_together() {
    let temp = temp_dir();
    Java::default()
        .package("dev.specta.generated")
        .layout(Layout::Files)
        .export_to(temp.path(), &types(), specta_serde::Format)
        .unwrap();

    let mut files = Vec::new();
    fn collect(path: &Path, files: &mut Vec<std::path::PathBuf>) {
        for entry in std::fs::read_dir(path).unwrap() {
            let path = entry.unwrap().path();
            if path.is_dir() {
                collect(&path, files);
            } else if path
                .extension()
                .is_some_and(|extension| extension == "java")
            {
                files.push(path);
            }
        }
    }
    collect(temp.path(), &mut files);
    assert_eq!(files.len(), 4);
    let refs = files
        .iter()
        .map(std::path::PathBuf::as_path)
        .collect::<Vec<_>>();
    compile_java(temp.path(), &refs);
}

#[test]
fn files_layout_removes_only_stale_generated_java() {
    let temp = temp_dir();
    let manual = temp.path().join("Manual.java");
    std::fs::write(&manual, "final class Manual {}\n").unwrap();

    let java = Java::default().layout(Layout::Files);
    java.export_to(temp.path(), &types(), IdentityFormat)
        .unwrap();
    java.export_to(temp.path(), &Types::default(), IdentityFormat)
        .unwrap();

    assert!(manual.exists());
    fn count_java(path: &Path) -> usize {
        std::fs::read_dir(path)
            .unwrap()
            .filter_map(Result::ok)
            .map(|entry| entry.path())
            .map(|path| {
                if path.is_dir() {
                    count_java(&path)
                } else {
                    usize::from(path.extension().is_some_and(|ext| ext == "java"))
                }
            })
            .sum()
    }
    assert_eq!(
        count_java(temp.path()),
        1,
        "only the manually maintained Java file remains"
    );
}

#[test]
fn files_layout_cleanup_is_scoped_to_the_configured_base_package() {
    let temp = temp_dir();
    let first = Java::default()
        .layout(Layout::Files)
        .package("com.acme.api");
    let second = Java::default()
        .layout(Layout::Files)
        .package("com.acme.admin");

    first
        .export_to(temp.path(), &types(), IdentityFormat)
        .unwrap();
    let first_file = temp
        .path()
        .join("com/acme/api")
        .join(module_path!().replace("::", "/"))
        .join("Model.java");
    assert!(first_file.exists());

    second
        .export_to(temp.path(), &types(), IdentityFormat)
        .unwrap();

    assert!(first_file.exists());
}

