use specta::{Type, TypeCollection};

// Simple user management types

#[derive(Type)]
struct User {
    id: u32,
    name: String,
    email: Option<String>,
    role: UserRole,
}

#[derive(Type)]
enum UserRole {
    Guest,
    Moderator,
    Admin,
}

#[derive(Type)]
struct CreateUserRequest {
    name: String,
    email: String,
    role: UserRole,
}

#[derive(Type)]
enum ApiResponse<T> {
    Ok(T),
    Err(String),
}

pub fn types() -> TypeCollection {
    TypeCollection::default()
        .register::<User>()
        .register::<UserRole>()
        .register::<CreateUserRequest>()
        .register::<ApiResponse<User>>()
}

#[cfg(not(test))]
fn main() {
    use specta_rescript::ReScript;

    let types = types();

    // Export with default settings (includes serde transformations)
    let rescript = ReScript::default();
    rescript
        .export_to(
            concat!(env!("CARGO_MANIFEST_DIR"), "/examples/generated/SimpleUsage.res"),
            &types,
        )
        .unwrap();

    println!("Simple types exported to SimpleUsage.res");

    // Also print to stdout
    let output = rescript.export(&types).unwrap();
    println!("{}", output);

    // Export with a custom header (e.g. for lint suppressions)
    let custom = ReScript::new().header(
        "// Generated by MyApp\n// @generated\n// biome-ignore-all lint: generated code",
    );
    custom
        .export_to(
            concat!(
                env!("CARGO_MANIFEST_DIR"),
                "/examples/generated/SimpleUsageCustomHeader.res"
            ),
            &types,
        )
        .unwrap();

    println!("Custom header variant exported to SimpleUsageCustomHeader.res");
}
